    private void total_hargaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_total_hargaMouseClicked
    int jumlahBaris = tabelData.getRowCount();
    int totalBiaya = 0;
    int jumlahBarang, hargaBarang ;
    TableModel tabelModel;
    tabelModel = tabelData.getModel();
    for (int i=0; i<jumlahBaris; i++){
        jumlahBarang = Integer.parseInt(tabelModel.getValueAt(i, 3).toString());
        hargaBarang = Integer.parseInt(tabelModel.getValueAt(i, 7).toString());
        totalBiaya = totalBiaya + (jumlahBarang+hargaBarang);
    };
total_harga.setText(String.valueOf(totalBiaya));       // TODO add your handling code here:
    }//GEN-LAST:event_total_hargaMouseClicked

